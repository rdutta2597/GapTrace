"""LCOV coverage file reader and parser.

Parses .info files generated by gcov/gtest and maps coverage data to decision points.
"""

import re
from pathlib import Path
from typing import Dict, List, Optional, Tuple
from dataclasses import dataclass

from gaptrace.models import DecisionPoint, Coverage


@dataclass
class LineCoverage:
    """Coverage data for a single line"""
    line_number: int
    execution_count: int
    is_covered: bool


class LcovReader:
    """Parser for lcov .info coverage files"""
    
    def __init__(self):
        """Initialize LCOV reader"""
        self.line_coverage: Dict[str, Dict[int, LineCoverage]] = {}
    
    def parse_file(self, lcov_path: str) -> Dict[str, Dict[int, LineCoverage]]:
        """Parse an lcov .info file and extract line coverage.
        
        Args:
            lcov_path: Path to .info file
            
        Returns:
            Dict mapping file_path -> line_number -> LineCoverage
            
        Raises:
            FileNotFoundError: If lcov file not found
            ValueError: If lcov file format is invalid
        """
        lcov_path = Path(lcov_path)
        
        if not lcov_path.exists():
            raise FileNotFoundError(f"LCOV file not found: {lcov_path}")
        
        coverage_data: Dict[str, Dict[int, LineCoverage]] = {}
        
        try:
            with open(lcov_path, 'r') as f:
                current_file: Optional[str] = None
                
                for line in f:
                    line = line.strip()
                    
                    # Skip empty lines and comments
                    if not line or line.startswith('#'):
                        continue
                    
                    # Parse file source record
                    if line.startswith('SF:'):
                        current_file = line[3:]  # Remove 'SF:' prefix
                        if current_file not in coverage_data:
                            coverage_data[current_file] = {}
                        continue
                    
                    # Parse line execution count record
                    if line.startswith('DA:') and current_file:
                        try:
                            # Format: DA:line_number,execution_count
                            parts = line[3:].split(',')
                            if len(parts) == 2:
                                line_num = int(parts[0])
                                exec_count = int(parts[1])
                                is_covered = exec_count > 0
                                
                                coverage_data[current_file][line_num] = LineCoverage(
                                    line_number=line_num,
                                    execution_count=exec_count,
                                    is_covered=is_covered
                                )
                        except (ValueError, IndexError):
                            # Skip malformed lines
                            continue
                    
                    # End of file coverage
                    if line == 'end_of_record':
                        current_file = None
        
        except IOError as e:
            raise ValueError(f"Failed to read LCOV file: {e}")
        
        self.line_coverage = coverage_data
        return coverage_data
    
    def get_file_coverage(self, file_path: str) -> Dict[int, LineCoverage]:
        """Get coverage for a specific file.
        
        Args:
            file_path: Source file path
            
        Returns:
            Dict mapping line_number -> LineCoverage
        """
        return self.line_coverage.get(file_path, {})
    
    def apply_to_decision_points(
        self,
        decision_points: List[DecisionPoint],
        file_path: str
    ) -> List[DecisionPoint]:
        """Apply coverage data to decision points.
        
        Args:
            decision_points: List of DecisionPoint objects to update
            file_path: Source file path to get coverage for
            
        Returns:
            Updated decision points with coverage information
        """
        coverage = self.get_file_coverage(file_path)
        
        for dp in decision_points:
            if dp.line_number in coverage:
                line_cov = coverage[dp.line_number]
                # Create Coverage object for this decision point
                dp.coverage = Coverage(
                    line_number=dp.line_number,
                    execution_count=line_cov.execution_count,
                    is_covered=line_cov.is_covered,
                    branches_total=1,
                    branches_covered=1 if line_cov.is_covered else 0
                )
        
        return decision_points
    
    def apply_to_parse_result(self, parse_result) -> None:
        """Apply coverage data to a ParseResult object.
        
        Args:
            parse_result: ParseResult object to update with coverage
        """
        from gaptrace.models import ParseResult
        
        file_path = parse_result.file_path
        coverage = self.get_file_coverage(file_path)
        
        # Update all decision points
        covered_count = 0
        for func in parse_result.functions.values():
            for dp in func.decision_points:
                if dp.line_number in coverage:
                    line_cov = coverage[dp.line_number]
                    dp.coverage = Coverage(
                        line_number=dp.line_number,
                        execution_count=line_cov.execution_count,
                        is_covered=line_cov.is_covered,
                        branches_total=1,
                        branches_covered=1 if line_cov.is_covered else 0
                    )
                    if dp.is_covered():
                        covered_count += 1
        
        # Update parse result stats
        parse_result.covered_decision_points = covered_count


def parse_lcov_and_merge(
    lcov_path: str,
    parse_result,
) -> None:
    """Convenience function to parse lcov file and merge with parse results.
    
    Args:
        lcov_path: Path to .info file
        parse_result: ParseResult object to merge coverage into
    """
    reader = LcovReader()
    reader.parse_file(lcov_path)
    reader.apply_to_parse_result(parse_result)
